    <p>
      Effective <strong>April 2025</strong>
    </p>

    <section>
      <h2>Information We Collect</h2>
      <p>
        When you log in with your <strong>Spotify account</strong>, Vinylify accesses data using the
        following Spotify permissions:
      </p>
      <ul>
        <li>user-read-email</li>
        <li>user-read-private</li>
        <li>user-library-read</li>
        <li>user-top-read</li>
        <li>user-follow-read</li>
        <li>playlist-read-private</li>
        <li>playlist-read-collaborative</li>
        <li>playlist-modify-private</li>
        <li>playlist-modify-public</li>
      </ul>
      <p>We use this access to retrieve:</p>
      <ul>
        <li>Your basic Spotify info (your email, display name, and profile picture)</li>
        <li>Your top tracks and artists</li>
        <li>Your saved songs and playlists</li>
        <li>The ability to create playlists for you</li>
      </ul>
    </section>

    <section>
      <h2>How We Use Your Data</h2>
      <ul>
        <li>Your top songs are shown in the app interface.</li>
        <li><strong>We only store your top songs if you click the "Share" button.</strong></li>
        <li>
          If you click "Create Playlist," we make a Spotify playlist for you with your (or someone's
          shared) top songs.
        </li>
        <li>We store your basic Spotify info to manage your account and shared content.</li>
      </ul>
    </section>

    <section>
      <h2>Data Visibility</h2>
      <ul>
        <li>
          Shared top songs are <strong>public</strong> and can be viewed by anyone on the internet.
        </li>
        <li>Your top songs are not stored unless you choose to share.</li>
      </ul>
    </section>

    <section>
      <h2>Your Controls</h2>
      <ul>
        <li>You can delete your shared top songs at any time.</li>
        <li>You can delete your Vinylify account, which removes all associated data.</li>
      </ul>
    </section>

    <section>
      <h2>Third Parties</h2>
      <p>
        We only use your data with Spotify. We do not share your information with any third-parties
        or third-party services.
      </p>
    </section>

    <section>
      <h2>Changes to This Policy</h2>
