    <h1 id="about-vinylify">About Vinylify</h1>
    <p>
      <a href="https://vinylify.naman.zip/">Vinylify</a> is a music service that allows
      you and your friends to discover and share your top songs through vinyl records.
    </p>
    <p>
      To get started, visit the <a href="https://vinylify.naman.zip/">website</a> and login with
      your Spotify account. Then, visit the <a href="https://vinylify.naman.zip/view">view</a> page
      to see your top songs. Add your top songs into a playlist by clicking the
      <em>add playlist</em>
      button, and share your top songs with your friends by clicking the <em>share</em> button. Copy
      your unique share link and send it to your friends to showcase your top songs!
    </p>
    <p>
      For example, click <a href="https://vinylify.naman.zip/share/RwNohef">here</a> to see my top songs!
    </p>
    <hr />
    <p>
      Vinylify is built and maintained by <a href="https://namanarora.net/">Naman Arora</a>, and was
      my submission for <a href="https://recordhacks.devpost.com/project-gallery">Record Hacks</a>,
      where the project won best design.
    </p>
    <p>
      Vinylify is fully open-source and the source code can be found on <a
        href="https://github.com/Naman-Arora/vinylify">GitHub</a
      >.
    </p>
    <p>
      If you find any bugs, or have any issues and/or suggestions, please make sure to either open
      an issue on GitHub or contact me at <a href="mailto:vinylify@namanarora.net"
        >vinylify@namanarora.net</a
      >.
    </p>
    <h2 id="tech-stack">Tech Stack</h2>
    <p>Vinylify is built using the following technologies:</p>
    <ul>
      <li><a href="https://kit.svelte.dev/">SvelteKit</a>, as the Javascript framework</li>
      <li>
        <a href="https://threlte.xyz/">Threlte</a> and <a href="https://threejs.org/">Three.js</a>,
        for 3D graphics
      </li>
      <li>
        <a href="https://tailwindcss.com/">Tailwind CSS</a> and
        <a href="https://daisyui.com/">DaisyUI</a>, for styling
      </li>
      <li>
        <a href="https://lucide.dev/">Lucide</a> and
        <a href="https://tabler-icons.io/">Tabler Icons</a>, for icons
      </li>
      <li><a href="https://hono.dev/">Hono</a>, for the api and api client</li>
      <li>
        <a href="https://developer.spotify.com/">Spotify Developer API</a>, for user music data
      </li>
      <li>
        <a href="https://better-auth.com/">Better-Auth</a>, for authentication and authorization
      </li>
      <li><a href="https://orm.drizzle.team/">Drizzle ORM</a>, for database management</li>
      <li>
        <a href="https://www.postgresql.org/">PostgreSQL</a>, hosted on
        <a href="https://neon.tech/">Neon</a>, as the database
      </li>
      <li><a href="https://vercel.com/">Vercel</a>, for deployment</li>
      <li><a href="https://github.com/">GitHub</a>, for continuous integration and deployment</li>
    </ul>
